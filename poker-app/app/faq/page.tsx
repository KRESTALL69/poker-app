const FAQ_TABS: Array<{ key: FaqTabKey; label: string }> = [
  { key: "general", label: "Общая информация" },
  { key: "common-rules", label: "Общий регламент" },
  { key: "tournament-rules", label: "Регламент турниров" },
  { key: "rating-rules", label: "Регламент рейтинга" },
];

function renderSections(sections: FaqArticleSection[]) {
  return (
    <div className="mt-6 space-y-4">
      {sections.map((section) => (
        <section
          key={section.title}
          className="rounded-[24px] border border-white/10 bg-white/[0.04] p-5"
        >
          <h2 className="text-xl font-semibold leading-snug">{section.title}</h2>

          <div className="mt-4 space-y-3 text-[15px] leading-7 text-white/78">
            {section.paragraphs.map((paragraph) => (
              <p key={paragraph}>{paragraph}</p>
            ))}

            {section.bullets ? (
              <ul className="space-y-2 pt-1 text-white/78">
                {section.bullets.map((bullet) => (
                  <li key={bullet} className="flex gap-3">
                    <span className="mt-[10px] h-1.5 w-1.5 rounded-full bg-white/45" />
                    <span>{bullet}</span>
                  </li>
                ))}
              </ul>
            ) : null}
          </div>
        </section>
      ))}
    </div>
  );
}

export default function FaqPage() {
  const [activeTab, setActiveTab] = useState<FaqTabKey>("general");

  useEffect(() => {
    const searchParams = new URLSearchParams(window.location.search);
    const tab = searchParams.get("tab");

    if (
      tab === "general" ||
      tab === "common-rules" ||
      tab === "tournament-rules" ||
      tab === "rating-rules"
    ) {
      setActiveTab(tab);
    }
  }, []);

  return (
    <main className="min-h-screen bg-black px-4 py-6 text-white">
      <div className="mx-auto max-w-3xl">
        <Link
          href="/"
          className="inline-flex items-center rounded-full border border-white/[0.08] bg-transparent px-3.5 py-2 text-sm text-white/65"
        >
          ← Назад
        </Link>

        <header className="mt-6 rounded-[28px] border border-white/10 bg-white/[0.04] p-6">
          <p className="text-xs uppercase tracking-[0.24em] text-white/45">
            ReRaise
          </p>
          <h1 className="mt-3 text-3xl font-semibold leading-tight">FAQ</h1>
          <p className="mt-3 max-w-2xl text-sm leading-6 text-white/65">
            Основная информация, регламенты и правила игрового пространства.
          </p>
        </header>

        <div className="mt-6 flex flex-wrap gap-2">
          {FAQ_TABS.map((tab) => (
            <button
              key={tab.key}
              type="button"
              onClick={() => setActiveTab(tab.key)}
              className={`rounded-full border px-4 py-2.5 text-sm ${
                activeTab === tab.key
                  ? "border-white/18 bg-white/10 text-white"
                  : "border-white/10 bg-white/[0.03] text-white/65"
              }`}
            >
              {tab.label}
            </button>
          ))}
        </div>

        {activeTab === "general"
          ? renderSections(GENERAL_FAQ_SECTIONS)
          : activeTab === "common-rules"
            ? renderSections(COMMON_RULES_SECTIONS)
            : activeTab === "tournament-rules"
              ? renderSections(TOURNAMENT_RULES_SECTIONS)
              : renderSections(RATING_RULES_SECTIONS)}
      </div>
    </main>
  );
}
